# Qual o papel do analisador léxico? Qual sua importância no contexto de avaliação de códigos?


# O analisador léxico, também conhecido como scanner, 
# tem como papel dividir a entrada de código em tokens, 
# pequenos fragmentos significativos. Esses tokens são usados pelo processador sintático 
# para construir a estrutura lógica do programa.

# A importância do analisador léxico na avaliação de códigos é fundamental, 
# pois ele é o primeiro passo na análise do programa, 
# estabelecendo a base para o processamento posterior. 
# Se o scanner não for capaz de dividir corretamente o código em tokens, 
# as etapas subsequentes serão afetadas, resultando em erros na avaliação do código. 
# Além disso, o analisador léxico também pode ser usado para realizar verificações de sintaxe simples, 
# como identificar identificadores inválidos ou constantes numéricas mal formadas.