# Defina token, lexema e padrão.

# Token: é a representação lógica de um conjunto de caracteres no código fonte. 
# É a unidade básica de informação que será passada para o próximo passo do processo de análise, geralmente o sintático.

# Lexema: é o nome dado ao conjunto de caracteres representado por um token. É a representação textual do token.

# Padrão: é uma expressão regular que é usada para identificar um lexema específico 
# e determinar o tipo de token que será gerado a partir dele. 
# O padrão é utilizado pelo analisador léxico para identificar lexemas no código fonte e gerar tokens correspondentes.